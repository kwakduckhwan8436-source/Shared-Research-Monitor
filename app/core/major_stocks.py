def major_symbols() -> list[str]:
    """대표 종목코드 리스트."""
    return [s for s, _, _ in MAJOR_STOCKS]


def major_name(symbol: str) -> str:
    """대표 종목의 폴백 이름(name_of 실패 시)."""
    for s, nm, _ in MAJOR_STOCKS:
        if s == symbol:
            return nm
    return symbol


def major_sector(symbol: str) -> str:
    """대표 종목의 업종."""
    for s, _, sec in MAJOR_STOCKS:
        if s == symbol:
            return sec
    return ""


def sectors() -> list[str]:
    """전체 업종 목록(중복 제거, 등장 순)."""
    seen: list[str] = []
    for _, _, sec in MAJOR_STOCKS:
        if sec not in seen:
            seen.append(sec)
    return seen
