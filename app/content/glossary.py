def categories() -> list[str]:
    seen = []
    for g in GLOSSARY:
        if g["cat"] not in seen:
            seen.append(g["cat"])
    return seen


def slugify(term: str) -> str:
    """URL 슬러그 — 영문 약어는 그대로, 한글은 그대로(URL 인코딩됨). 공백·괄호 제거."""
    import re
    s = (term or "").strip()
    s = re.sub(r"\([^)]*\)", "", s)        # 괄호 내용 제거
    s = s.replace(" ", "").replace("/", "-")
    return s


def term_by_slug(slug: str):
    s = (slug or "").strip()
    for g in GLOSSARY:
        if slugify(g["term"]) == s or g["term"] == s:
            return g
    return None


